//C program to print the sum of two numbers
#include <stdio.h>

int main(){
    int a,b;
    printf("Enter first number:\n");
    scanf("%d",&a);

    printf("Enter second number:\n");
    scanf("%d",&b);

    int sum;
    sum = a + b;
    printf("The sum of two numbers is %d",sum);

    return 0;
}





